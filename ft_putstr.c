#include "printf.h"

void	ft_putstr(char *str, int *i)
{
	int	j;

	j = 0;
	if (str == NULL)
		return (ft_putstr("(null)", i));
	while (str[j])
	{
		write(1, &str[j], 1);
		j++;
		(*i)++;
	}
}
